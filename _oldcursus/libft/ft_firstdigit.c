#include "libft.h"

char	ft_firstdigit(int n, int q)
{
	char	c;

	if (n >= 0 & n < 10)
		c = n + '0';
	if (n >= 10)
		c = n / (10 * q) + '0';
	if (n < 0)
		c = '-';
	return (c);
}
