#include "libft.h"

char	*ft_suprwsbeg(char const *s)
{
	int		i;

	i = 0;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\n')
		i++;
	while (s[i])
	{
		if (s[i])
			return ((char *)&s[i]);
		i++;
	}
	return ("\0");
}
